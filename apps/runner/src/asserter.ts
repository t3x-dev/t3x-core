/** @deprecated Use AssertionInput instead */
export interface GenerateAssertionsInput {
  run_id: string;
  leaf?: { id: string; type: 'deploy' | 'eval'; content?: string };
  inputs?: Record<string, unknown>;
  run_report: { output?: Record<string, unknown>; meta?: { latency_ms?: number; tokens?: number } };
  evidence_pack: {
    n8n_output?: Record<string, unknown>;
    n8n_meta?: { latency_ms?: number; tokens?: number };
    error?: string | null;
  };
  eval_rules?: {
    expected_output?: string;
    must_contain?: string[];
    must_not_contain?: string[];
    custom_checks?: string[];
  };
}

/** @deprecated Use EvalResult.score instead */
export interface EvalMetrics {
  correctness?: number;
  coverage?: number;
  efficiency?: number;
}

/** @deprecated Use AssertionOutput instead */
export interface AssertionResult {
  run_id: string;
  assertions: Assertion[];
  metrics: EvalMetrics;
  summary: string;
  generated_at: string;
}
